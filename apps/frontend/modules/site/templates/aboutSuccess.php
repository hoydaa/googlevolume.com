<p>Google Volume is a web page that enables you to continuesly track the result sizes of your queries.</p>

<p>With Google Volume;</p>

<ul>
	<li>You can create a report containing multiple queries</li>
	<li>You can see the result sizes of your queries on charts</li>
	<li>You can see all the queries on the same chart as different labels, which enables you to compare them.</li>
	<li>You can share the reports you created with the other people by marking them as public.</li>
	<li>You can search public queries</li>
	<li>You can sort public queries by date, popularity, statibility and data amount
		<ul>
			<li>Date: sorts the reports according to the last modification date.</li>
			<li>Popularity: sorts the reports according to view count.</li>
			<li>Stability: sorts the reports according to their stability. (Average standard deviation is the deciding criteria for the stability of the report.)</li>
		</ul>
	</li>
	<li>You can schedule a mail for your report with different frequencies; daily, weekly, monthly.</li>
</ul>

<p>Currently Google Volume is searching your queries over Google which is not a surprise as the name indicates:) But we are planning to include Yahoo as well.</p>